	(
		SocieteId INT NOT NULL,
		FamilleId INT NOT NULL,
		CodeNature NVARCHAR(50),
		CodeJournal NVARCHAR(50),
		EnCompta BIT
	)
	-- Récupération des paramétrages
	INSERT INTO @tmp(SocieteId,FamilleId,CodeNature,CodeJournal,EnCompta)
	SELECT s.SocieteId,F.FamilleOperationDiverseId,A.Code_nature,A.Codejournal,A.Cmde_en_compta_Oui_Non
	FROM FRED_SOCIETE S
	INNER JOIN @tmpRZB_Association_Fod_Natures_Journaux A ON S.Code = A.Code_societe 
	INNER JOIN FRED_FAMILLE_OPERATION_DIVERSE F ON F.Code = A.code_famille_od AND S.SocieteId = F.SocieteId;

	SELECT TOP 1 @SocieteId = SocieteId FROM @tmp;

	--Remise à 0 du paramétrages
	UPDATE FRED_JOURNAL
	SET ParentFamilyODWithOrder = 0,ParentFamilyODWithoutOrder = 0,DateModification = GETDATE()
	WHERE SocieteId = @SocieteId;

	UPDATE FRED_NATURE
	SET ParentFamilyODWithOrder = 0,ParentFamilyODWithoutOrder = 0,DateModification = GETDATE()
	WHERE SocieteId = @SocieteId;

	-- Pour chaque paramétrage
	WHILE EXISTS(SELECT TOP(1) 1 FROM @tmp)
	BEGIN
		SELECT @SocieteId=SocieteId, @FamilleId= FamilleId, @CodeNature = CodeNature, @CodeJournal = CodeJournal, @EnCompta = EnCompta FROM @tmp;
		
		IF(@SocieteId IS NOT NULL AND @FamilleId IS NOT NULL AND @SocieteId IS NOT NULL)
		BEGIN
		--MAJ de la famille d'un journal
			UPDATE FRED_JOURNAL
			SET DateModification = GETDATE(),
				ParentFamilyODWithOrder = 
							( CASE 
								WHEN (@EnCompta = 1) THEN @FamilleId
								ELSE ParentFamilyODWithOrder
								END),
				ParentFamilyODWithoutOrder = 
							( CASE 
								WHEN (@EnCompta = 0) THEN @FamilleId
								ELSE ParentFamilyODWithoutOrder
								END)
			WHERE Code = @CodeJournal AND SocieteId = @SocieteId;

		--MAJ de la famille d'une nature
			UPDATE FRED_NATURE
			SET DateModification = GETDATE(),
				ParentFamilyODWithOrder = 
							( CASE 
								WHEN (@EnCompta = 1) THEN @FamilleId
								ELSE ParentFamilyODWithOrder
								END),
				ParentFamilyODWithoutOrder = 
							( CASE  
								WHEN (@EnCompta = 0) THEN @FamilleId
								ELSE ParentFamilyODWithoutOrder
								END)
			WHERE Code = @CodeNature AND SocieteId = @SocieteId;
			
			
		END

